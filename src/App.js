let lastIndex = -1;
let lastType = '';

function generateQuote(type) {
    let quotes;
    switch(type) {
        case 'sweet':
            quotes = sweetQuotes;
            break;
        case 'comfort':
            quotes = comfortQuotes;
            break;
        case 'missYou':
            quotes = missYouQuotes;
            break;
        case 'apologize':
            quotes = apologizeQuotes;
            break;
        case 'cute':
            quotes = cuteQuotes;
            break;
    }

    const quoteDisplay = document.getElementById('quoteDisplay');
    
    let randomIndex;
    do {
        randomIndex = Math.floor(Math.random() * quotes.length);
    } while (randomIndex === lastIndex && type === lastType);
    
    lastIndex = randomIndex;
    lastType = type;

    quoteDisplay.classList.add('fade');
    setTimeout(() => {
        quoteDisplay.textContent = quotes[randomIndex];
        quoteDisplay.classList.remove('fade');
    }, 300);
}

// 添加键盘快捷键
document.addEventListener('keydown', (event) => {
    switch(event.code) {
        case 'Digit1':
            generateQuote('sweet');
            break;
        case 'Digit2':
            generateQuote('comfort');
            break;
        case 'Digit3':
            generateQuote('missYou');
            break;
        case 'Digit4':
            generateQuote('apologize');
            break;
        case 'Digit5':
            generateQuote('cute');
            break;
    }
}); 
